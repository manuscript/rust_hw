.section .text
.globl _start
_start:
    # Регистры и их назначение:
    # %rbx — первое слагаемое (число 25)
    # %rcx — второе слагаемое (число 27)
    # %rax — код возврата из main (результат сложения)

    # Загружаем первое число в регистр %rbx
    movq $25, %rbx          # Первое число: 25

    # Загружаем второе число в регистр %rcx
    movq $27, %rcx          # Второе число: 27

    # Складываем %rbx и %rcx, результат сохраняется в %rbx
    addq %rcx, %rbx         # %rbx = %rbx + %rcx = 25 + 27 = 52

    # Копируем результат в %rdi (код завершения)
    movq %rbx, %rdi         # %rdi = 52 (код завершения)

    # Устанавливаем номер системного вызова exit в %rax
    movq $60, %rax         # Системный вызов exit (60)

    # Выполняем системный вызов
    syscall

