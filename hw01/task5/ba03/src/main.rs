use std::env;


fn main() {
    // Получаем итератор аргументов командной строки и пропускаем первый (имя программы)
    let mut args: Vec<String> = env::args().skip(1).collect();
    
    // Сортируем аргументы в лексикографическом порядке (с учётом регистра: A < a)
    args.sort();
    
    // Выводим каждый аргумент на отдельной строке
    for arg in args {
        println!("{}", arg);
    }
}

